
package org.zhaoqian.common.exception;

public class CRMException extends Exception {

	private static final long serialVersionUID = 1926439629486459955L;

	public CRMException() {

		super();
	}

	public CRMException(String message, Throwable cause) {

		super(message, cause);
	}

	public CRMException(String message) {

		super(message);
	}

	public CRMException(Throwable cause) {

		super(cause);
	}

}
